class Dumka3TimeStepper(object):
    """Third-order "DUMKA" timesteppers.

    Alexei A. Medovikov, "High order explicit methods for parabolic equations"
    BIT1998, Vol. 38, No.2, pp.372-390

    Note: If you use this code for published results, Dr. Medovikov expects (but
    does not require) a citation of the publication above.
    """

    dt_fudge_factor = 3

    POLYNOMIAL_COUNT = 13
    # 14 polynomials are available, but polynomial 13 fails
    # accuracy test?

    def __init__(self, pol_index=None, dtype=numpy.float64, rcon=None, 
            vector_primitive_factory=None, atol=0, rtol=0):
        if pol_index > self.POLYNOMIAL_COUNT:
            raise ValueError("invalid polynomial index specified")
        self.pol_index=pol_index

        self.dtype = numpy.dtype(dtype)
        if vector_primitive_factory is None:
            from hedge.vector_primitives import VectorPrimitiveFactory
            self.vector_primitive_factory = VectorPrimitiveFactory()
        else:
            self.vector_primitive_factory = vector_primitive_factory

        from pytools import match_precision
        self.scalar_type = match_precision(
                numpy.dtype(numpy.float64), self.dtype).type

        self.adaptive = bool(atol or rtol)
        self.atol = atol
        self.rtol = rtol

        self.coeff = numpy.array(_COEF, dtype=self.scalar_type)

        # diagnostics init
        from pytools.log import IntervalTimer, EventCounter
        timer_factory = IntervalTimer
        if rcon is not None:
            timer_factory = rcon.make_timer

        self.timer = timer_factory(
                "t_dumka3", "Time spent doing algebra in Dumka3")
        self.flop_counter = EventCounter(
                "n_flops_dumka3", "Floating point operations performed in Dumka3")

        self.last_eps = 0
        self.last_dt = 0

    def get_stability_relevant_init_args(self):
        return (self.pol_index,)

    def setup(self, eigenvalue_estimate, dt, pol_index=None):
        # find degree of the polynomials to be used
        if pol_index is None:
            pol_index = 0
            while (pol_index!=self.POLYNOMIAL_COUNT-1) \
                    and 2*dt/eigenvalue_estimate > _STAB_REG[pol_index]:
                pol_index += 1

            if pol_index > 0 and (
                (_STAB_REG[pol_index-1]/_N_DEG[pol_index-1])*_N_DEG[pol_index]
                > 2*dt/eigenvalue_estimate):
                pol_index -= 1
        else:
            if pol_index > self.POLYNOMIAL_COUNT:
                raise ValueError("invalid polynomial index specified")

        n_pol_degree = _N_DEG[pol_index]

        if (dt is not None and eigenvalue_estimate is not None) and \
                dt > _STAB_REG[pol_index]*eigenvalue_estimate/2:
            from warnings import warn
            warn("timestep too big")

        self.pol_index = pol_index

    def add_instrumentation(self, logmgr):
        logmgr.add_quantity(self.timer)
        logmgr.add_quantity(self.flop_counter)

    def __call__(self, y, t, dt, rhs_func):
        try:
            lc2 = self.linear_combiner_2
            lc3 = self.linear_combiner_3
            ip = self.ip
        except AttributeError:
            from hedge.tools import count_dofs
            self.dof_count = count_dofs(y)

            vpf = self.vector_primitive_factory
            lc2 = self.linear_combiner_2 = vpf.make_linear_combiner(
                    self.dtype, self.scalar_type, y, arg_count=2)
            lc3 = self.linear_combiner_3 = vpf.make_linear_combiner(
                    self.dtype, self.scalar_type, y, arg_count=3)

            ip = self.ip = vpf.make_inner_product(y)

        def norm(a):
            return numpy.sqrt(ip(a, a))

        dt = self.scalar_type(dt)

        pol_index = self.pol_index
        if pol_index is None:
            raise RuntimeError("must call setup() or set pol_index before timestepping")

        rhs = rhs_func(t, y)

        coeff = self.coeff

        start_index = _INDEX_FIRST[pol_index]-1
        end_index = _INDEX_LAST[pol_index]

        start_y = y
        start_rhs = rhs
        start_t = t

        vec_op_count = 0

        retry_step = True
        while retry_step:
            n_pol = 0

            for k in range(start_index, end_index):
                sub_timer = self.timer.start_sub_timer()

                n_pol = n_pol+3
                idx = 6*k
                a_21 = dt*coeff[idx]
                c_2 = a_21
                a_31 = dt*coeff[idx+1]
                a_32 = dt*coeff[idx+2]
                c_3 = a_31+a_32
                a_41 = dt*coeff[idx+3]
                a_42 = dt*coeff[idx+4]
                a_43 = dt*coeff[idx+5]
                c_4 = a_41+a_42+a_43

                y = lc2((1, y), (a_21, rhs))

                t2 = t+c_2

                sub_timer.stop().submit()

                # marker W ******************************
                # markers correspond to comments in doc/notes/dumka3.cpp

                z1 = rhs_func(t2, y)

                sub_timer = self.timer.start_sub_timer()
                if n_pol == _N_DEG[pol_index]:
                    r = dt*(coeff[idx+1]-coeff[idx])
                    y = lc3((1, y), (r, rhs), (a_32, z1))
                else:
                    y = lc2((1, y), (a_32, z1))

                # marker X ******************************
                if self.adaptive and n_pol == _N_DEG[pol_index]:
                    tmp_2 = (c_4-c_2)/2
                    z1 = lc2((c_3/2,z1), (-tmp_2,rhs));
                sub_timer.stop().submit()

                # marker Y ******************************
                rhs = rhs_func(t+c_3, y)

                sub_timer = self.timer.start_sub_timer()
                if self.adaptive and n_pol == _N_DEG[pol_index]:
                    z1 = lc2((tmp_2, rhs), (1,z1))

                # marker Z ******************************
                y = lc2((1, y), (a_43, rhs))
                sub_timer.stop().submit()

                t += c_4
                if k+1 != end_index or self.adaptive:
                    rhs = rhs_func(t, y)

            if self.adaptive:
                sub_timer = self.timer.start_sub_timer()
                normalization = self.atol + self.rtol*max(
                            norm(y), norm(start_y))

                z1 = lc2((-c_3/(2*normalization),rhs), (1/normalization,z1)) 

                next_dt, retry_step = self.compute_new_step_size(z1, dt)
                if t + next_dt == t:
                    raise RuntimeError("stepsize underflow")

                if retry_step:
                    y = start_y
                    rhs = start_rhs
                    dt = next_dt
                    t = start_t

                sub_timer.stop().submit()
            else:
                retry_step = False

        self.flop_counter.add((end_index-start_index)*self.dof_count*9)

        if self.adaptive:
            return y, t, dt, next_dt
        else:
            return y

    def compute_new_step_size(self, z, dt):
        from hedge.tools import count_dofs
        eps = numpy.sqrt(self.ip(z, z)/count_dofs(z))
        if eps == 0:
           eps = 1e-14

        fracmin = 0.1
        frac = (1/eps)**(1/3)

        rejected = numpy.isnan(eps) or eps > 1
        if rejected:
            if self.last_dt == 0:
                frac = fracmin
            else:
                frac = 0.8*min(1, max(fracmin, 0.8*frac))
        else:
            if self.last_eps > 0 and self.last_dt > 0:
                frac2 = self.last_eps**(1/3)*frac*frac*(dt/self.last_dt)
                frac = min(frac, frac2)

            frac = min(2, max(fracmin, 0.8*frac))

            self.last_eps = eps
            self.last_dt = dt

        dt = frac*dt

        return dt, rejected




